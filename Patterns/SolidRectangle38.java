package Patterns;

public class SolidRectangle38 {
    public static void main(String[] args) {
        int n = 3, m = 8;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= m; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
